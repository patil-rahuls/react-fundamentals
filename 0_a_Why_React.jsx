// Why React? ////////////////////////////////////////////////
//////////////////////////////////////////////////////////////

// Open Source
// Reusable UI Components
// Usefull for SPA
// Supports Server Side Rendering.
// SEO friendly

// Performance Optimization:
// Working directly with the DOM for manipulation is slow
// because every change triggers expensive browser processes
// like reflow (recalculating layouts) and repaint (drawing
// pixels). The VDOM minimizes these by batching updates.

// Declarative Programming:
// Instead of writing manual instructions to find and update
// elements (e.g., document.getElementById), you simply
// describe the desired state of the UI. React handles the
// "how" of updating the screen.

// Cross-Platform Consistency:
// Because the VDOM is just a set of JavaScript objects, it
// can be rendered to different targets, which is why the
// same React principles work for web, mobile (React Native),
// and even VR.


// Interview Questions:

// "Why React over Angular or Vue?"
// Flexibility. React is a library, not a rigid framework,
// giving you the freedom to choose your own tools, libraries,
// architecture e.g. for routing and state management.

// "Why React?"
// 'separation of concerns'. It’s a component-based library
// that allows for a declarative way of building UIs.
// Instead of manually updating the DOM, I describe the state
// I want, and React’s Virtual DOM efficiently handles the
// updates. This 'separation of concerns' makes the code more
// predictable and easier to debug.
